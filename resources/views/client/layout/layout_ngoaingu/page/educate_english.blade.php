                            <p> &emsp; Khoa Ngoại Ngữ – BQLĐATL Trường Đại học Du lịch Sài Gòn là tiền thân của Trường Cao đẳng Văn hóa Nghệ thuật và Du lịch Sài Gòn được thành lập cùng với sự thành lập của trường 17 tháng 10 năm 2006. Từ đó đến nay, Khoa đã đào tạo hàng ngàn sinh viên ra trường, đã góp phần tạo nguồn nhân lực giỏi về Ngoại Ngữ cho Khu vực Miền Nam.

                                Khoa Ngoại Ngữ – BQLĐATL Trường Đại học Du lịch Sài Gòn ra đời mở ra cơ hội tốt hơn cho người học có điều kiện học tập ở trình độ cao thuộc các chuyên ngành Ngoại Ngữ, góp phần nâng cao chất lượng nguồn nhân lực giỏi về Ngoại Ngữ ở khu vực miền Nam nói riêng và Du lịch Việt Nam nói chung. Sự ra đời của Khoa Ngoại Ngữ – BQLĐATL Trường Đại học Du lịch Sài Gòn cũng tạo thêm cơ hội để khai thác hiệu quả hơn các nguồn lực hiện có của BQLĐATL Trường Đại học Du lịch Sài
                                Gòn, góp phần thực hiện tốt chức năng nhiệm vụ của một đại học trọng điểm đa ngành ở miền Nam.

                                Khoa Ngoại Ngữ bậc Đại học gồm có 2 ngành: Ngôn Ngữ Anh và Đông Phương học; bậc Cao đẳng gồm 4 ngành: Tiếng Anh, Tiếng Nhật, Tiếng Hàn, Tiếng Trung; bậc Trung cấp chuyên nghiệp gồm 4 ngành: Tiếng Anh, Tiếng Nhật, Tiếng Hàn, Tiếng Trung. Đây được xem là những ngành nghề thu hút nhân lực hiện nay và đáp ứng nhu cầu xã hội và sự phát triển của đất nước.
                            </p>

